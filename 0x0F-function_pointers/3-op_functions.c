#include "3-calc.h"

/**
 * op_add - Function for add.
 * @number1: First number.
 * @number2: Second number.
 * Return: Always 0.
 */

int op_add(int number1, int number2)
{
	return (number1 + number2);
}
/**
 * op_sub - Function for subtract.
 * @number1: First number.
 * @number2: Second number.
 * Return: Always 0.
 */
int op_sub(int number1, int number2)
{
	return (number1 - number2);
}
/**
 * op_mul - Function for multiply.
 * @number1: First number.
 * @number2: Second number.
 * Return: Always 0.
 */
int op_mul(int number1, int number2)
{
	return (number1 * number2);
}
/**
 * op_div - Function for divide.
 * @number1: First number.
 * @number2: Second number.
 * Return: Always 0.
 */
int op_div(int number1, int number2)
{
	return (number1 / number2);
}
/**
 * op_mod - Function for get the modulus of a number.
 * @number1: First number.
 * @number2: Second number.
 * Return: Always 0.
 */
int op_mod(int number1, int number2)
{
	return (number1 % number2);
}
